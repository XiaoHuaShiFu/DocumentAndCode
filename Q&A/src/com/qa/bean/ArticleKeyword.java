package com.qa.bean;

public class ArticleKeyword {
	
	private int from;
	private int to;
	private String title;
	private boolean ramdom; 
	public int getFrom() {
		return from;
	}
	public void setFrom(int from) {
		this.from = from;
	}
	public int getTo() {
		return to;
	}
	public void setTo(int to) {
		this.to = to;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public boolean isRamdom() {
		return ramdom;
	}
	public void setRamdom(boolean ramdom) {
		this.ramdom = ramdom;
	}
	
}
